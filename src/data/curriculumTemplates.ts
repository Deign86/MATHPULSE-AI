const competency = (
  code: string,
  description: string,
  confidence: CurriculumConfidence = 'high',
  sourceReferenceIds: readonly string[] = G11_PRIMARY_SOURCE_IDS,
): CompetencyMapping => ({
  code,
  description,
  confidence,
  sourceReferenceIds: [...sourceReferenceIds],
});

const buildCompetencyIndex = (items: CompetencyMapping[]): Record<string, CompetencyMapping> => {
  return items.reduce<Record<string, CompetencyMapping>>((acc, item) => {
    acc[item.code] = item;
    return acc;
  }, {});
};

const pickCompetencies = (
  index: Record<string, CompetencyMapping>,
  codes: readonly string[],
): CompetencyMapping[] => {
  return codes.map((code) => {
    const item = index[code];
    if (!item) {
      throw new Error(`Missing competency definition for code: ${code}`);
    }
    return item;
  });
};

